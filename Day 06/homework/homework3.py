a = 10
b = 3

# მათემატიკური ოპერატორები
print(a + b)    # + შეკრება
print(a - b)    # - გამოკლება
print(a * b)    # * გამრავლება
print(a / b)    # / გაყოფა (ათწილადად)
print(a // b)   # // მთლიანი გაყოფა
print(a % b)    # % ნაშთი
print(a ** b)   # ** ხარისხში აყვანა

# შედარების ოპერატორები
print(a == b)   # == ტოლია?
print(a != b)   # != არ არის ტოლი?
print(a > b)    # > მეტია?
print(a < b)    # < ნაკლებია?
print(a >= b)   # >= მეტია ან ტოლია?
print(a <= b)   # <= ნაკლებია ან ტოლია?
